from natural.helpers import _make

# Модуль P-4: Умножение многочлена на x^k, k — натуральное или 0
# Автор: Никита, группа 5387


def MUL_Pxk_P(p, k):
    """
    Умножает многочлен p на x^k.

    Параметры:
        p: {'m': степень, 'coeffs': [Q0, Q1, ..., Qm]}
           coeffs идут от МЛАДШЕЙ степени к СТАРШЕЙ
        k: обычный int >= 0 (не длинное число)

    Возвращает:
        многочлен степени m+k

    Идея: умножить на x^k — значит сдвинуть все коэффициенты
    на k позиций вправо, добавив k нулей в начало списка.
    Например: (3 + 2x) * x^2 = 3x^2 + 2x^3
    coeffs было [3, 2], стало [0, 0, 3, 2]
    """
    if k < 0:
        raise ValueError("k должно быть >= 0")

    # рациональный ноль: 0/1
    zero_q = {
        'num': {'sign': 0, 'n': 0, 'digits': [0]},
        'den': _make([1])
    }

    # добавляем k нулей перед существующими коэффициентами
    new_coeffs = [zero_q] * k + p['coeffs']
    new_degree = p['m'] + k

    return {
        'm': new_degree,
        'coeffs': new_coeffs
    }