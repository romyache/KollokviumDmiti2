def POZ_Z_D(z: dict) -> int:
    """
    Z-2 POZ_Z_D — определение знака целого числа.

    Параметры:
        z (dict): целое число в формате {'sign': ..., 'n': ..., 'digits': [...]}

    Возвращает:
        int: 2 — положительное, 0 — равное нулю, 1 — отрицательное
    """
    # Проверяем, является ли число нулём: все цифры равны 0
    if all(d == 0 for d in z['digits']):
        return 0
    if z['sign'] == 1:
        return 1  # отрицательное
    return 2  # положительное