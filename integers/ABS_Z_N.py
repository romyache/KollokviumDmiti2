def ABS_Z_N(z: dict) -> dict:
    """
    ABS_Z_N — абсолютная величина целого числа(Z-1).

    Параметры:
        z (dict): целое число в формате {'sign': ..., 'n': ..., 'digits': [...]}

    Возвращает:
        dict: натуральное число {'n': ..., 'digits': [...]} — |z|
    """
    return {
        'n': z['n'],
        'digits': z['digits'][:]
    }