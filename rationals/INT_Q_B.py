def INT_Q_B(q):
    """
    Проверяет, является ли рациональное число целым.
    Рациональное число целое тогда и только тогда, когда знаменатель = 1.

    Параметры:
        q: {'num': целое Z, 'den': натуральное N}

    Возвращает:
        True — если число целое (знаменатель равен 1)
        False — иначе
    """
    den = q['den']

    # знаменатель равен 1, если он однозначный и его единственная цифра — 1
    is_one = (den['n'] == 0 and den['digits'][0] == 1)
    return is_one