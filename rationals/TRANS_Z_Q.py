from natural.helpers import _make

# Модуль Q-3: Преобразование целого числа в дробное
# Автор: Никита, группа 5387


def TRANS_Z_Q(z):
    """
    Преобразует целое число в рациональное (дробь со знаменателем 1).

    Параметры:
        z: {'sign': ..., 'n': ..., 'digits': [...]}  — целое число

    Возвращает:
        {'num': z, 'den': натуральное 1}
    """
    # знаменатель — просто единица в формате натурального числа
    denominator = _make([1])

    return {
        'num': z,          # числитель — само целое число
        'den': denominator  # знаменатель = 1
    }